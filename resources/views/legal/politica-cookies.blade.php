    <h1>Política de Cookies</h1>

    <p><strong>Última modificación:</strong> Noviembre 2024</p>

    <p>En <strong>Fragora</strong> utilizamos cookies para facilitar y mejorar la experiencia de navegación del usuario, así
        como para personalizar los contenidos que le mostramos. La presente Política de Cookies tiene como finalidad
        informar al usuario sobre el uso de cookies en nuestra Página Web y APP, así como sobre la posibilidad de configurar
        las preferencias relacionadas con las cookies.</p>

    <h2>¿Qué son las cookies?</h2>
    <p>Las cookies son pequeños archivos de texto que se almacenan en el dispositivo del usuario (ordenador, móvil, tablet,
        etc.) cuando accede a nuestra Página Web o APP. Estas cookies permiten que nuestra web recuerde sus acciones y
        preferencias durante un tiempo determinado, para que no tenga que volver a configurarlas cada vez que regrese.</p>

    <h2>¿Qué tipos de cookies utilizamos?</h2>
    <p>En <strong>Fragora</strong> utilizamos las siguientes categorías de cookies:</p>
    <ul>
        <li><strong>Cookies técnicas</strong>: Son necesarias para la navegación de la Página Web o la APP, como las que
            permiten la autenticación del usuario o el carrito de compra.</li>
        <li><strong>Cookies de personalización</strong>: Permiten recordar preferencias o configuraciones específicas del
            usuario, como el idioma o la ubicación.</li>
        <li><strong>Cookies analíticas</strong>: Se utilizan para realizar análisis estadísticos de la navegación, con el
            fin de mejorar la funcionalidad de la Página Web o la APP.</li>
        <li><strong>Cookies publicitarias</strong>: Permiten mostrar anuncios personalizados, según los intereses del
            usuario, y gestionar las campañas publicitarias.</li>
    </ul>

    <h2>¿Por qué utilizamos cookies?</h2>
    <p>Las cookies son utilizadas por <strong>Fragora</strong> con los siguientes fines:</p>
    <ul>
        <li>Facilitar la navegación y la funcionalidad de la Página Web o APP.</li>
        <li>Personalizar la experiencia del usuario, recordando preferencias de navegación.</li>
        <li>Realizar análisis estadísticos sobre el uso de la Página Web para mejorar su rendimiento y funcionalidad.</li>
        <li>Mostrar contenido publicitario adaptado a los intereses del usuario.</li>
    </ul>

    <h2>¿Cómo puede gestionar las cookies?</h2>
    <p>El usuario puede gestionar el uso de cookies en cualquier momento, tanto aceptando como rechazando su instalación. Si
        desea modificar la configuración de cookies, puede hacerlo a través de las opciones de su navegador. Además, tiene
        la posibilidad de configurar las categorías de cookies que desea activar mediante el botón de ajustes a
        continuación:</p>

    <p><a href="#">Clic aquí para configurar las cookies</a></p>

    <h2>¿Cómo puede desactivar las cookies?</h2>
    <p>El usuario puede configurar su navegador para que le avise de la recepción de cookies o para impedir que se instalen
        en su dispositivo. Sin embargo, le informamos que la desactivación de ciertas cookies podría afectar al
        funcionamiento de la Página Web y APP, y es posible que algunas funciones no estén disponibles.</p>

    <p>Para desactivar las cookies, siga las instrucciones de su navegador en los siguientes enlaces:</p>
    <ul>
        <li><a href="https://support.google.com/chrome/answer/95647?hl=es" target="_blank">Google Chrome</a></li>
        <li><a href="https://support.mozilla.org/es/kb/habilitar-y-deshabilitar-cookies" target="_blank">Mozilla Firefox</a>
        </li>
        <li><a href="https://support.microsoft.com/es-es/help/17442/windows-internet-explorer-delete-manage-cookies"
                target="_blank">Microsoft Internet Explorer</a></li>
        <li><a href="https://support.apple.com/kb/PH21411?locale=es_ES" target="_blank">Safari</a></li>
    </ul>

    <h2>¿Se cederán sus datos a terceros?</h2>
    <p>En <strong>Fragora</strong> no compartimos la información obtenida mediante las cookies con terceros, excepto en los
        casos en que sea necesario para cumplir con la ley o en los que se utilicen servicios de terceros como herramientas
        analíticas o publicitarias que cumplen con las normativas de privacidad aplicables.</p>

    <h2>Modificaciones en la Política de Cookies</h2>
    <p>Nos reservamos el derecho de modificar esta Política de Cookies en cualquier momento. Si realizamos algún cambio
        importante, le informaremos a través de nuestra Página Web o la APP. Le recomendamos que revise periódicamente esta
        política para estar al tanto de cómo utilizamos las cookies.</p>

